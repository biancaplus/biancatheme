            window.onload = () => {
                // 卡片线框跟随
                const cards = document.querySelectorAll('.cards-wrap .card');
                const pointer = document.querySelector('.card-pointer');
                let pointerShown = false;

                for (const card of cards) {
                    card.addEventListener('mouseenter', () => {
                        const w = card.offsetWidth;
                        const h = card.offsetHeight;
                        const x = card.offsetLeft;
                        const y = card.offsetTop;

                        pointer.style.setProperty('--x', x + 'px');
                        pointer.style.setProperty('--y', y + 'px');
                        pointer.style.setProperty('--w', w + 'px');
                        pointer.style.setProperty('--h', h + 'px');

                        if (!pointerShown) {
                            pointer.style.opacity = '1';
                            pointer.style.visibility = 'visible';
                            pointerShown = true;
                        }
                    });
                }
            };


            window.addEventListener('scroll', function() {
                // 获取滚动条的位置，如果>0且不是文章详情页，则给header添加fix-header类名
                if(window.scrollY > 0 && !document.querySelector('.single-content')){
                    document.querySelector('.site-header').classList.add('fix-header');
                }else{
                    document.querySelector('.site-header').classList.remove('fix-header');
                }

                // 展示返回顶部按钮
                if(window.scrollY > 400){
                    document.querySelector('#back-to-top').classList.add('show');
                }else{
